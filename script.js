       // Function to shuffle an array
function shuffleArray(array) {
  for (let i = array.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [array[i], array[j]] = [array[j], array[i]];
  }
}

const container = document.getElementById("flashcard-container");
const nextButton = document.getElementById("next-button");
console.log(nextButton)

// Combine and shuffle all the cards from both categories
let combinedCards = [];
flashcards.forEach(category => {
  category.images.forEach(image => {
      combinedCards.push({ answer: category.answer, image });
  });
});
shuffleArray(combinedCards);

let currentCardIndex = 0;

function displayNextCard() {
  if (currentCardIndex < combinedCards.length) {
      const flashcard = combinedCards[currentCardIndex];
      const card = document.createElement("div");
      card.classList.add("flashcard");

      const cardInner = document.createElement("div");
      cardInner.classList.add("card-inner");

      const cardFront = document.createElement("div");
      cardFront.classList.add("card-front");

      const cardBack = document.createElement("div");
      cardBack.classList.add("card-back");

      const image = document.createElement("img");

      // Get the image for the current flashcard
      image.src = flashcard.image;

      cardFront.appendChild(image);
      cardBack.textContent = flashcard.answer;

      cardInner.appendChild(cardFront);
      cardInner.appendChild(cardBack);

      card.appendChild(cardInner);

      let flipped = false;

      card.addEventListener("click", () => {
          card.classList.toggle("flip");
          flipped = !flipped;
      });

      container.innerHTML = '';
      container.appendChild(card);

      currentCardIndex++;

      if (currentCardIndex === combinedCards.length) {
          nextButton.style.display = "none"; // Hide the next button when all cards are shown
      }
  }
}

nextButton.addEventListener("click", displayNextCard);

// Initial display of the first card
displayNextCard();